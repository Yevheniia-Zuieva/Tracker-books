"""
Модуль реалізації кастомних механізмів валідації даних.

Цей модуль містить логіку перевірки вхідних даних на відповідність
встановленим правилам безпеки та бізнес-вимогам.
"""

import re

from django.core.exceptions import ValidationError
from django.utils.translation import gettext as _


class ComplexPasswordValidator:
    """
    Валідатор для забезпечення високого рівня складності паролів.

    Цей клас інтегрується в стандартну систему валідації Django Auth.
    Він вимагає, щоб пароль відповідав критеріям наявності символів різних регістрів,
    цифрових значень та спеціальних знаків.

    Методи класу реалізують інтерфейс, необхідний для роботи в списку
    `AUTH_PASSWORD_VALIDATORS` у файлі налаштувань проекту.
    """

    def validate(self, password, user=None):
        r"""
        Аналізує структуру пароля за допомогою регулярних виразів.

        Метод проводить послідовну перевірку рядка на наявність трьох типів обов'язкових
        елементів. Якщо хоча б один критерій не виконано, генерується виняток
        із відповідним кодом помилки.

        Args:
            password (str): Текстовий рядок пароля, який потребує перевірки.
            user (User, optional): Екземпляр користувача, для якого валідується пароль.
                За замовчуванням `None` (використовується Django при зміні пароля).

        Raises:
            ValidationError: Виникає, якщо пароль не містить:
                - Великих літер (`[A-Z]`)
                - Цифр (`\d`)
                - Спеціальних символів (напр. `!@#$%^&*`)
        """
        # Перевірка наявності принаймні однієї великої літери (A-Z)
        if not re.search(r"[A-Z]", password):
            raise ValidationError(
                _("Пароль повинен містити хоча б одну велику літеру."),
                code="password_no_upper",
            )

        # Перевірка наявності принаймні однієї цифри (0-9)
        if not re.search(r"\d", password):
            raise ValidationError(
                _("Пароль повинен містити хоча б одну цифру."),
                code="password_no_number",
            )

        # Перевірка наявності спеціальних символів для підвищення ентропії пароля
        # Використовується набір стандартних знаків пунктуації та символів
        if not re.search(r'[!@#$%^&*()_+\-=\[\]{};\':"\\|,.<>\/?]', password):
            raise ValidationError(
                _("Пароль повинен містити хоча б один спеціальний символ."),
                code="password_no_symbol",
            )

    def get_help_text(self):
        """Повертає текст підказки з описом вимог до пароля.

        Returns:
            str: Рядок з інструкцією щодо створення пароля.

        """
        return _(
            "Пароль не відповідає вимогам. Пароль має бути не менше 8 символів і "
            "містити великі та малі літери, цифри, а також спеціальні символи. "
            "Будь ласка, спробуйте ще раз."
        )
