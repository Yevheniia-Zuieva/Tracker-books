"""
Модуль управління поштовими сповіщеннями.

Містить кастомні класи для генерації та відправки електронних листів
користувачам, інтегровані з бібліотекою Djoser для аутентифікації.
"""

from djoser import email


class CustomPasswordResetEmail(email.PasswordResetEmail):
    """Кастомний клас для відправки листа відновлення пароля.

    Перевизначає стандартний метод Djoser для впровадження користувацьких
    змінних контексту, таких як URL фронтенду.
    """

    def get_context_data(self):
        """
        Створює набір даних (контекст) для рендерингу шаблону листа.

        Цей метод додає адресу фронтенду та назву сайту до контексту,
        що дозволяє динамічно генерувати посилання для скидання пароля.

        Returns:
            dict: Словник параметрів, який передається у Django-шаблон листа.
                Включає ключі:
                - `domain`: Адреса фронтенд-застосунку (localhost:5173).
                - `site_name`: Брендована назва сервісу.
                - `uid`, `token`, `user` та інші стандартні поля Djoser.
        """
        context = super().get_context_data()

        # Перевизначення домену для перенаправлення на React-додаток
        context["domain"] = "localhost:5173"
        context["site_name"] = "Tracker Books"

        return context
